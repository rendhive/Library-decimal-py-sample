from decimal import Decimal

item_price = Decimal('75.50')
quantity = Decimal('3')
total_cost = item_price * quantity
print("Biaya total untuk 3 barang:", total_cost)
# Fungsi: Menghitung total biaya berdasarkan harga item dan kuantitas.
# Kondisi: Ketika Anda menghitung total biaya untuk pembelian secara grosir.
