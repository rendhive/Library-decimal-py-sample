from decimal import Decimal

number1 = Decimal('10.0')
number2 = Decimal('3.0')
result = number1 / number2
print("Hasil pembagian 10.0 / 3.0:", result)
# Fungsi: Menghitung pembagian dua angka desimal.
# Kondisi: Ketika Anda perlu menghitung tarif atau pembagian jumlah dan ingin mendapatkan hasil tepat.
