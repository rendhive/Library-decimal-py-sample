from decimal import Decimal

numbers = [Decimal('100.25'), Decimal('200.75'), Decimal('50.50')]
average = sum(numbers) / len(numbers)
print("Rata-rata:", average)
# Fungsi: Menghitung rata-rata dari sekumpulan angka desimal.
# Kondisi: Ketika Anda ingin menghitung rata-rata dari transaksi atau nilai desimal lainnya.
