from decimal import Decimal

number1 = Decimal('0.1')
number2 = Decimal('0.2')
result = number1 + number2
print("Hasil penjumlahan 0.1 + 0.2:", result)
# Fungsi: Menghitung penjumlahan dua angka desimal dengan presisi tinggi.
# Kondisi: Ketika menghitung uang atau nilai akurat lainnya yang memerlukan lebih dari dua angka desimal.
