from decimal import Decimal

numbers = [Decimal('10.5'), Decimal('15.75'), Decimal('5.25')]
max_value = max(numbers)
min_value = min(numbers)
print("Nilai maksimum:", max_value)
print("Nilai minimum:", min_value)
# Fungsi: Menemukan nilai maksimum dan minimum dalam kumpulan angka desimal.
# Kondisi: Ketika Anda ingin mendapatkan nilai ekstrim dalam laporan atau analisis.
